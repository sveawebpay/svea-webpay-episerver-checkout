        public string GetStateName(string twoLetterCountryCode, string stateCode)
        {
            if (twoLetterCountryCode == null)
            {
                throw new ArgumentNullException(nameof(twoLetterCountryCode));
            }
            if (stateCode == null)
            {
                throw new ArgumentNullException(nameof(stateCode));
            }

            if (_regions.TryGetValue(twoLetterCountryCode.ToUpperInvariant(), out IDictionary<string, string> countryRegiondictionary))
            {
                if (countryRegiondictionary.TryGetValue(stateCode.ToUpperInvariant(), out string stateName))
                {
                    return stateName;
                }
            }
            return string.Empty;
        }

        public string GetStateCode(string twoLetterCountryCode, string stateName)
        {
            if (twoLetterCountryCode == null)
            {
                throw new ArgumentNullException(nameof(twoLetterCountryCode));
            }
            if (stateName == null)
            {
                throw new ArgumentNullException(nameof(stateName));
            }

            if (_regions.TryGetValue(twoLetterCountryCode, out IDictionary<string, string> countryRegiondictionary))
            {
                var region = countryRegiondictionary.FirstOrDefault(x => x.Value.Equals(stateName, StringComparison.InvariantCultureIgnoreCase));
                if (!region.Equals(default(KeyValuePair<string, string>)))
                {
                    return region.Key;
                }
            }
            return string.Empty;
        }

